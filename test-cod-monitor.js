require("dotenv").config({ path: "./.env" });

const path = require("path");
const { spawn } = require("child_process");
const { processCodMonitorResults } = require("./alertEngine");

function parseArgs() {
  const args = process.argv.slice(2);
  const out = {
    pythonBin: process.env.COD_MONITOR_PYTHON_BIN || "python",
    scriptPath:
      process.env.COD_MONITOR_SCRIPT_PATH ||
      path.join(__dirname, "cod_monitor", "main.py"),
    brands: [],
    forceAlertIfNone: true,
  };

  for (let i = 0; i < args.length; i += 1) {
    const arg = args[i];
    if (arg === "--python-bin" && args[i + 1]) {
      out.pythonBin = args[i + 1];
      i += 1;
    } else if (arg === "--script-path" && args[i + 1]) {
      out.scriptPath = args[i + 1];
      i += 1;
    } else if (arg === "--brands" && args[i + 1]) {
      out.brands = args[i + 1]
        .split(",")
        .map((brand) => brand.trim())
        .filter(Boolean);
      i += 1;
    } else if (arg === "--no-force-alert") {
      out.forceAlertIfNone = false;
    }
  }

  return out;
}

function runCodMonitor({ pythonBin, scriptPath, brands }) {
  const args = [scriptPath, "--json-output"];
  if (brands.length) {
    args.push("--brands", ...brands);
  }

  return new Promise((resolve, reject) => {
    const child = spawn(pythonBin, args, {
      cwd: __dirname,
      env: process.env,
      windowsHide: true,
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });

    child.stderr.on("data", (chunk) => {
      const text = chunk.toString();
      stderr += text;
      process.stderr.write(text);
    });

    child.on("error", reject);

    child.on("close", (code) => {
      try {
        const payloadText = stdout.trim();
        if (!payloadText) {
          throw new Error("COD monitor produced no JSON payload on stdout");
        }

        const payload = JSON.parse(payloadText);
        resolve({ code, payload, stderr });
      } catch (err) {
        reject(
          new Error(
            `Failed to parse COD monitor JSON output: ${err.message}${stderr ? ` | stderr: ${stderr.trim()}` : ""}`,
          ),
        );
      }
    });
  });
}

function forceAlertIntoPayload(payload) {
  const cloned = JSON.parse(JSON.stringify(payload));
  const target = cloned.brand_results.find((result) => result.status === "ok");
  if (!target) {
    throw new Error("Cannot force a COD alert: no successful brand result found.");
  }

  const overall = target.overall || {};
  const currentValue = Number.isFinite(Number(overall.today_cod_pct))
    ? Number(overall.today_cod_pct)
    : 50;
  const baselineValue = Number.isFinite(Number(overall.yesterday_cod_pct))
    ? Number(overall.yesterday_cod_pct)
    : Math.max(0, currentValue - 5);
  const deltaValue = Number.isFinite(Number(overall.delta_cod_pct))
    ? Number(overall.delta_cod_pct)
    : Number((currentValue - baselineValue).toFixed(2));

  const syntheticAlert = {
    brand: target.brand,
    level: "overall",
    delta: deltaValue,
    today_cod_pct: currentValue,
    yesterday_cod_pct: baselineValue,
    today_total_orders: overall.today_total_orders ?? 0,
    message:
      "Forced COD test alert generated by test-cod-monitor.js to validate the email pipeline.",
  };

  target.alerts = [syntheticAlert];
  target.alert_count = 1;

  cloned.total_alerts = 1;
  return cloned;
}

async function main() {
  const options = parseArgs();

  console.log("🧪 Running COD monitor test flow...");
  console.log(`   Python: ${options.pythonBin}`);
  console.log(`   Script: ${options.scriptPath}`);
  console.log(
    `   Brands: ${options.brands.length ? options.brands.join(", ") : "all configured brands"}`,
  );

  const { code, payload } = await runCodMonitor(options);
  console.log(
    `   COD monitor completed with exit code ${code} | alerts=${payload.total_alerts} | errors=${payload.total_errors}`,
  );

  let effectivePayload = payload;
  if (Number(payload.total_alerts || 0) === 0 && options.forceAlertIfNone) {
    console.log(
      "   No live COD alerts were returned, so one forced test alert will be injected to exercise email delivery.",
    );
    effectivePayload = forceAlertIntoPayload(payload);
  }

  const result = await processCodMonitorResults(effectivePayload);
  console.log("✅ COD test flow complete.");
  console.log(JSON.stringify(result, null, 2));
}

main().catch((err) => {
  console.error("❌ COD test flow failed:", err.message);
  process.exit(1);
});
